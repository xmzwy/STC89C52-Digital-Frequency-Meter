/* 魹ｽﾈｴ魹ｽﾒｺ魹ｽ魹ｽﾗｼ魹ｽ魹ｽ魹ｽﾃ｣魹ｽﾃｦ魹ｽ魹ｽ筌ｩ */
void LcdWaitReady()
{
    uint8 sta;

    LCD12864_DB = 0xFF;
    LCD12864_RS = 0;
    LCD12864_RW = 1;
    do {
        LCD12864_E = 1;
        sta = LCD12864_DB;      // 魹ｽ魹ｽﾈ｡ﾗｴﾌｬ魹ｽ魹ｽ
        LCD12864_E = 0;
    } while (sta & 0x80);       // bit7ﾎｪ1魹ｽ魹ｽﾊｾﾒｺ魹ｽ魹ｽﾃｦ魹ｽ魹ｽ魹ｽﾘｸ魹ｽ魹ｽ魹ｽ魹ｽﾖｱ魹ｽ魹ｽﾎｪ0
}

/* 魹ｽ魹ｽﾒｺ魹ｽ魹ｽﾐｴ魹ｽ魹ｽﾒｻ魹ｽﾖｽ魹ｽ魹ｽ魹ｽ魹ｽ詝ｬcmd-魹ｽ魹ｽﾐｴ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾖｵ */
void LcdWriteCmd(uint8 cmd)
{
    LcdWaitReady();
    LCD12864_RS = 0;            // RS=0 ﾑ｡魹ｽ魹ｽﾖｸ魹ｽ魹ｽﾄｴ魹ｽ魹ｽ魹ｽ
    LCD12864_RW = 0;            // RW=0 ﾐｴ魹ｽ魹ｽ魹ｽ魹ｽ
    LCD12864_DB = cmd;          // 魹ｽﾍｳ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
    LCD12864_E  = 1;            // ﾊｹ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
    LCD12864_E  = 0;
}

/* 魹ｽ魹ｽﾒｺ魹ｽ魹ｽﾐｴ魹ｽ魹ｽﾒｻ魹ｽﾖｽ魹ｽ魹ｽ魹ｽ魹ｽﾝ｣魹ｽdat-魹ｽ魹ｽﾐｴ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾖｵ */
void LcdWriteDat(uint8 dat)
{
    LcdWaitReady();
    LCD12864_RS = 1;            // RS=1 ﾑ｡魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾝｼﾄｴ魹ｽ魹ｽ魹ｽ
    LCD12864_RW = 0;            // RW=0 ﾐｴ魹ｽ魹ｽ魹ｽ魹ｽ
    LCD12864_DB = dat;          // 魹ｽﾍｳ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
    LCD12864_E  = 1;            // ﾊｹ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
    LCD12864_E  = 0;
}

/*
* 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾆ｣魹ｽLcdShowString
* 魹ｽ魹ｽ    魹ｽﾜ｣魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾄｻ魹ｽ魹ｽﾖｸ魹ｽ魹ｽﾎｻ魹ｽ魹ｽ魹ｽ魹ｽﾊｾﾒｻ魹ｽ魹ｽ魹ｽﾖｷ魹ｽ魹ｽ魹ｽ
* 魹ｽ魹ｽ    魹ｽ魹ｽ魹ｽ魹ｽstr - 魹ｽ魹ｽ魹ｽ魹ｽﾊｾ魹ｽﾖｷ魹ｽ魹ｽ魹ｽﾖｸ魹ｽ魹ｽ
*           x   - 魹ｽ魹ｽﾄｻ魹ｽ魹ｽﾊｾ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ(魹ｽ魹ｽ魹ｽ魹ｽﾎｪ魹ｽ魹ｽﾎｻ)
*           y   - 魹ｽ魹ｽﾄｻ魹ｽ魹ｽﾊｾ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ(魹ｽ魹ｽ魹ｽ魹ｽﾎｪ魹ｽ魹ｽﾎｻ)
* ﾗ｢    魹ｽ筌ｺ魹ｽﾖｷ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽC魹ｽ魹ｽ魹ｽﾔｹ豺ｶ魹ｽ魹ｽ魹ｽ魹ｽ'¥0'-NULLﾎｪ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾊｶ
*           x魹ｽ魹ｽy魹ｽ魹ｽ魹ｽ魹ｽ檥ｽ16魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾈｫ魹ｽ魹ｽ魹ｽﾖｷ魹ｽ(16*16pixel)ﾎｪ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾎｻ
*/
void LcdShowString(uint8 x, uint8 y, uint8 *str)
{
    uint8 addr;

    // 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾖｷ魹ｽ魹ｽ魹ｽﾊｾﾎｻ魹ｽﾃｵ魹ｽDDRAM魹ｽ魹ｽﾖｷ
    x >>= 4;   // x魹ｽ魹ｽ魹ｽ魹ｽ16魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾎｪ魹ｽﾖｷ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
    y >>= 4;   // y魹ｽ魹ｽ魹ｽ魹ｽ16魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾎｪ魹ｽﾖｷ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
    if (y >= 2)
    {
        y -= 2;
        x += 8;
    }
    addr = y * 16 + x;

    // 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾊｼDDRAM魹ｽ魹ｽﾖｷ魹ｽ魹ｽ魹ｽ魹ｽﾊｼﾐｴ魹ｽ魹ｽ魹ｽﾖｷ魹ｽ魹ｽ魹ｽ
    LcdWriteCmd(0x30);          // 魹ｽﾐｻ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾖｸ魹ｽ贒ｯ
    LcdWriteCmd(0x80 | addr);   // 魹ｽ魹ｽ魹ｽ魹ｽDDRAM魹ｽ魹ｽﾖｷ
    while (*str != '¥0')
    {
        LcdWriteDat(*str);      // 魹ｽ魹ｽ魹ｽﾖｽ魹ｽﾐｴ魹ｽ魹ｽ
        str++;
    }
}

/*
* 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾆ｣魹ｽLcdShowImage
* 魹ｽ魹ｽ    魹ｽﾜ｣魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾄｻ魹ｽ魹ｽ魹ｽ魹ｽﾊｾﾒｻ魹ｽ魹ｽﾍｼ魹ｽ魹ｽ
* 魹ｽ魹ｽ    魹ｽ魹ｽ魹ｽ魹ｽimg - 魹ｽ魹ｽ魹ｽ魹ｽﾊｾﾍｼ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾖｸ魹ｽ魹ｽ
*           x   - 魹ｽ魹ｽﾄｻ魹ｽ魹ｽﾊｾ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ(魹ｽ魹ｽ魹ｽ魹ｽﾎｪ魹ｽ魹ｽﾎｻ)
*           y   - 魹ｽ魹ｽﾄｻ魹ｽ魹ｽﾊｾ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ(魹ｽ魹ｽ魹ｽ魹ｽﾎｪ魹ｽ魹ｽﾎｻ)
*           w   - ﾍｼ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ(魹ｽ魹ｽ魹ｽ魹ｽﾎｪ魹ｽ魹ｽﾎｻ)
*           h   - ﾍｼ魹ｽ魹ｽﾟｶ魹ｽ(魹ｽ魹ｽ魹ｽ魹ｽﾎｪ魹ｽ魹ｽﾎｻ)
* ﾗ｢    魹ｽ筌ｺx魹ｽ魹ｽw魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ16魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽGDRAMﾋｮﾆｽﾑｰﾖｷ魹ｽ魹ｽﾎｻﾎｪ2魹ｽﾖｽﾚ｣魹ｽ
*           y魹ｽ魹ｽhﾎｪ0‾63ﾖｮ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾖｵ
*/
void LcdShowImage(uint8 x, uint8 y, uint8 w, uint8 h, uint8 *img)
{
    int16 i;
    uint8 xi, yi;
    uint8 xt, yt;

    x >>= 4;
    w >>= 3;
    i = 0;
    LcdWriteCmd(0x36);
    for (yi = 0; yi < h; yi++)
    {
        yt = y + yi;
        xt = x;
        if (yt >= 32)
        {
            yt -= 32;
            xt += 8;
        }
        LcdWriteCmd(0x80 | yt);
        LcdWriteCmd(0x80 | xt);
        for (xi = 0; xi < w; xi++)
        {
            LcdWriteDat(img[i++]);
        }
    }
}

/*
* 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾆ｣魹ｽLcdClearArea
* 魹ｽ魹ｽ    魹ｽﾜ｣魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾄｻ魹ｽﾏｵ魹ｽﾒｻ魹ｽ魹ｽﾍｼ魹ｽ魹ｽ魹ｽ魹ｽﾊｾ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ0x00魹ｽ魹ｽ
* 魹ｽ魹ｽ    魹ｽ魹ｽ魹ｽ魹ｽx - 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾊｼ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ(魹ｽ魹ｽ魹ｽ魹ｽﾎｪ魹ｽ魹ｽﾎｻ)
*           y - 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾊｼ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ(魹ｽ魹ｽ魹ｽ魹ｽﾎｪ魹ｽ魹ｽﾎｻ)
*           w - 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ(魹ｽ魹ｽ魹ｽ魹ｽﾎｪ魹ｽ魹ｽﾎｻ)
*           h - 魹ｽ魹ｽ魹ｽ魹ｽﾟｶ魹ｽ(魹ｽ魹ｽ魹ｽ魹ｽﾎｪ魹ｽ魹ｽﾎｻ)
*/
void LcdClearArea(uint8 x, uint8 y, uint8 w, uint8 h)
{
    uint8 xi, yi;
    uint8 xt, yt;

    x >>= 4;
    w >>= 3;
    LcdWriteCmd(0x36);
    for (yi = 0; yi < h; yi++)
    {
        yt = y + yi;
        xt = x;
        if (yt >= 32)
        {
            yt -= 32;
            xt += 8;
        }
        LcdWriteCmd(0x80 | yt);
        LcdWriteCmd(0x80 | xt);
        for (xi = 0; xi < w; xi++)
        {
            LcdWriteDat(0x00);
        }
    }
}

