void InitLcd12864()
{
    uint8 x, y;

    // === 魹ｽﾖｷ魹ｽﾄ｣ﾊｽ魹ｽ魹ｽﾊｼ魹ｽ魹ｽ ===
    LcdWriteCmd(0x30);   // 魹ｽ魹ｽ魹ｽ魹ｽﾖｸ魹ｽ贒ｯ魹ｽ魹ｽ8ﾎｻ魹ｽ魹ｽ魹ｽ魹ｽ
    LcdWriteCmd(0x01);   // 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾊｾ
    LcdWriteCmd(0x02);   // 魹ｽ魹ｽﾖｷ魹ｽ魹ｽﾎｻ
    LcdWriteCmd(0x0C);   // 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾊｾ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾘ｣魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾋｸ

    // === ﾍｼ魹ｽ魹ｽﾄ｣ﾊｽ魹ｽ魹ｽﾊｼ魹ｽ魹ｽ ===
    LcdWriteCmd(0x34);
    for (y = 0; y < 32; y++)
    {
        LcdWriteCmd(0x80 | y);
        LcdWriteCmd(0x80 | 0);
        for (x = 0; x < 32; x++)
        {
            LcdWriteDat(0x00);
        }
    }
    LcdWriteCmd(0x36);
}
